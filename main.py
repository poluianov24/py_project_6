'''Напишіть функцію, що обчислює факторіал кожного елемента списку цілих.
Функція повертає новий список, який містить отримані факторіали.'''

def Func(x):
    res = []
    for y in x:
        el = 1
        for i in range(1, y+1):
            el *= i
        res.append(el)
    return res

print(Func([0, 1, 2, 3, 4, 5, 6, 7, 8]))