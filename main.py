'''Напишіть функцію, що видаляє зі списку цілих деяке задане число.
З функції потрібно повернути кількість видалених елементів.'''

def Func(x, y):
    res = 0
    for i in x:
        if i == y:
            x.remove(y)
            res += 1
    return res

print(Func([11, 2, 7, 8, 5, 2, 3, 4, 6, 2, 5], 2))